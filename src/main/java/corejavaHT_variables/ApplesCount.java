package corejavaHT_variables;

public class ApplesCount {

	public static void main(String[] args) {
		// Problem-1 Daniel has 3 apples; Amber has 2 apples. How many apples do Daniel
		// and Amber have together?
		int danielApples = 3;
		int amberApples = 2;

		int totalApples = danielApples + amberApples;
		System.out.println("Total apples that Daniel and Amber have together are " + totalApples);

		
		// Problem-2. Daniel has 3.5 apples and Amber has 2.5 apples. How many apples do
		// Daniel and Amber have together?
		double danielApples1 = 3.5;
		double amberApples2 = 2.5;

		double totalApples2 = danielApples1 + amberApples2;
		System.out.println("Total apples that Daniel and Amber have together are " + totalApples2);
	}

}
